    private void gjejFilmin(int id, int sasia) {
        Filmi filmi = null;
        for(Filmi f : filmat) {
            if(f.getFilmiId() == id) {
                filmi = f;
                break;
            }
        }
        if(filmi != null) {
            addToTable(filmi, sasia);
            kalkuloTotalin();
        }
    }
    
    private void addToTable(Filmi f, int sasia) {
        int rowNr = rezTable.getModel().getRowCount();
        
        for(int i=0;i<rowNr;i++){
            if(rezTable.getValueAt(i, 0)== null){
                if(exists(f, sasia)) {
                    return;
                }else {
                    rezTable.setValueAt(f, i, 0);
                    rezTable.setValueAt(sasia, i, 1);
                    rezTable.setValueAt(5.0, i, 2);
                    return;
                }
            }
        }
        
    }
    
    private boolean exists(Filmi f, int sasia) {
            int rowNr = rezTable.getModel().getRowCount();
            for(int i = 0;i<rowNr;i++) {
               if(rezTable.getValueAt(i, 0) != null) { 
                    Filmi test = (Filmi)rezTable.getValueAt(i, 0);
                    if(test.equals(f)) {
                        int currentSasia = (int) rezTable.getValueAt(i, 1);
                        System.out.println(currentSasia);
                        currentSasia = sasia;  
                        System.out.println(currentSasia);
                        rezTable.setValueAt(currentSasia, i, 1);
                        System.out.println(rezTable.getValueAt(i, 1));
                        return true;        
                    }
               }

            }
        return false;
    }
    
