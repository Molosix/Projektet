    private double kalkuloTotalin() {
        double totali = 0;
        int rowNr = rezTable.getModel().getRowCount();
        
        for(int i=0;i<rowNr;i++){
            if(rezTable.getValueAt(i, 0)!= null) {
               double cmimi = (double)rezTable.getValueAt(i, 2);
               int sasia = (int)rezTable.getValueAt(i, 1);
               double itemTotal = cmimi * sasia;
               totali += itemTotal;
            }
        }
        String s = String.valueOf(totali);
        totaliField.setText(s);
        return totali;
               
    }
