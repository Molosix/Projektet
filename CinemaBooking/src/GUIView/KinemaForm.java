    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
       try{
            int row = tabela.getSelectedRow();
            if(row == -1){
              Kinema k = new Kinema();
              
              k.setEmri(emriField.getText());
              k.setQyteti(qytetiField.getText());
              k.setRruga(rrugaField.getText());
              k.setZip(Integer.parseInt(zipField.getText()));
              
              
             
              
              
         
              kr.create(k);
            }else{
                Kinema k = ktm.getKinema(row);
                k.setEmri(emriField.getText());
                k.setQyteti(qytetiField.getText());
                k.setRruga(rrugaField.getText());
                k.setZip(Integer.parseInt(zipField.getText()));
                
               
                kr.edit(k);
            }
            clear();
            loadTable();
        }catch(CFormException ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "E dhena ekziston!");
        }
    }//GEN-LAST:event_saveButtonActionPerformed

    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        int row = tabela.getSelectedRow();
        if(row != -1){
            Object[] ob = {"Po","Jo"};
            int i = JOptionPane.showOptionDialog(this,"A deshironi ta fshini?","Fshirja", JOptionPane.OK_OPTION, JOptionPane.QUESTION_MESSAGE, null, ob, ob[1]);
            if(i == 0){
                Kinema k = ktm.getKinema(row);
                try{
                    kr.delete(k);
                }catch(CFormException ex){
                    JOptionPane.showMessageDialog(this,ex.getMessage());
                }
                clear();
                loadTable();
            }else{
                clear();
            }
        }else{
            JOptionPane.showMessageDialog(this, "Nuk keni selektuar asgje per te fshire!");
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

