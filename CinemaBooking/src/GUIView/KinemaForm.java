    public void loadTable(){
        try{
            List<Kinema> lista = kr.findAll();
            ktm.addList(lista);
            tabela.setModel(ktm);
            ktm.fireTableDataChanged();
            tabela.repaint();
        }catch(CFormException ex){
            Logger.getLogger(PuntoriForm.class.getName()).log(Level.SEVERE,null,ex);
        }
    }
     private void tabelaSelectedIndexChange(){
        final ListSelectionModel rowSM = tabela.getSelectionModel();
        rowSM.addListSelectionListener(new ListSelectionListener(){

            @Override
            public void valueChanged(ListSelectionEvent e) {
                if(e.getValueIsAdjusting()){
                    return;
                }
                
                ListSelectionModel rowSM = (ListSelectionModel) e.getSource();
                int selectedIndex = rowSM.getAnchorSelectionIndex();
                if(selectedIndex > -1){
                    Kinema k = ktm.getKinema(selectedIndex);
                    kinemaField.setText(k.getKinemaID().toString());
                    emriField.setText(k.getEmri());
                    qytetiField.setText(k.getQyteti());
                    rrugaField.setText(k.getRruga());
                    zipField.setText(String.valueOf(k.getZip()));
                    
                }
            }
        });
    }

