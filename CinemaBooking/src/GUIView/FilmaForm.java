    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
         try{
            int row = tabela.getSelectedRow();
            if(row == -1){
              Filmi f = new Filmi();
              
              f.setKoha(kohaField.getText());
              f.setTitulli(titulliField.getText());
              f.setDescription(descriptionField.getText());
              List<Kinema> kinemate = kr.findAll();
              
              f.setKinema(kinemate.get(0));
              
              
         
              fr.create(f);
            }else{
              Filmi f = ftm.getFilmi(row);
              f.setKoha(kohaField.getText());
              f.setTitulli(titulliField.getText());
              f.setDescription(descriptionField.getText());
              
               
                fr.edit(f);
            }
            clear();
            loadTable();
        }catch(CFormException ex){
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "E dhena ekziston!");
        }
    }//GEN-LAST:event_saveButtonActionPerformed

