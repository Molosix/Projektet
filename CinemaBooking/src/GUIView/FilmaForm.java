    public void loadTable(){
        try{
            List<Filmi> lista = fr.findAll();
            ftm.addList(lista);
            tabela.setModel(ftm);
            ftm.fireTableDataChanged();
        }catch(CFormException ex){
            Logger.getLogger(PuntoriForm.class.getName()).log(Level.SEVERE,null,ex);
        }
    }
    private void tabelaSelectedIndexChange(){
        final ListSelectionModel rowSM = tabela.getSelectionModel();
        rowSM.addListSelectionListener(new ListSelectionListener(){

            @Override
            public void valueChanged(ListSelectionEvent e) {
                if(e.getValueIsAdjusting()){
                    return;
                }
                
                ListSelectionModel rowSM = (ListSelectionModel) e.getSource();
                int selectedIndex = rowSM.getAnchorSelectionIndex();
                if(selectedIndex > -1){
                    Filmi f = ftm.getFilmi(selectedIndex);
                    filmiIdField.setText(f.getFilmiId().toString());
                    kohaField.setText(f.getKoha());
                    titulliField.setText(f.getTitulli());
                    descriptionField.setText(f.getDescription());
                    kinemaField.setText(f.getKinema().toString());
                }
            }
        });
    }
