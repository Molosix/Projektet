    public void loadComboBox(){
        try{
            List<Kinema> lista = kp.findAll();
            kcbm.add(lista);
            kinemaComboBox.setModel(kcbm);
            kinemaComboBox.repaint();
        }catch(CFormException ex){
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
