    private void registerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerButtonActionPerformed
        String emriReg = "[A-Za-z]{2,20}";
        String usernameReg = "\\w{2,20}";
        String passwordReg = "^(?=.*[0-9])[a-zA-Z0-9!@#$%^&*._-]{6,20}$";
        
        try {
           PuntoriRepository pr = new PuntoriRepository();
            p = new Puntori();
           
           p.setEmri(emriField.getText());
           if(!matches(emriReg,emriField.getText())) {
            JOptionPane.showMessageDialog(this, "Emri duhet te permbaje vetem shkronja");
            return;
           }
            
           p.setMbiemri(mbiemriField.getText());
           if(!matches(emriReg,mbiemriField.getText())) {
            JOptionPane.showMessageDialog(this, "Emri duhet te permbaje vetem shkronja");
            return;
           }
           
           p.setUsername(usernameField.getText());
           if(!matches(usernameReg,usernameField.getText())) {
            JOptionPane.showMessageDialog(this, "Emri duhet te permbaje vetem shkronja");
            return;
           }
          
           p.setPassword(new String(passwordField.getPassword()));
           if(!matches(passwordReg,p.getPassword())) {
            JOptionPane.showMessageDialog(this, "Passwordi duhet te kete prej 6-20 karaktere dhe se paku nje numer");
            return;
           }
           p.setAutoriteti(autoritetiField.getText());
           if(!autoritetiField.getText().equalsIgnoreCase("admin") && !autoritetiField.getText().equalsIgnoreCase("user")) {
            JOptionPane.showMessageDialog(this, "Duhet te jete admin ose user");
            return;
           }
           
           p.setKinema((Kinema)kinemaComboBox.getSelectedItem());
           //
            
           
           pr.create(p);
           new LoginForm().setVisible(true);
           this.setVisible(false);
            JOptionPane.showMessageDialog(this, "Regjistrimi u be me sukses");
           
           
       }catch(CFormException cfe) {
           JOptionPane.showMessageDialog(this, "Ekziston ne databaze!");
           cfe.printStackTrace();
       }
    }//GEN-LAST:event_registerButtonActionPerformed

